package com.android.platforming.clazz;

public class ParentItem {
    private String month;
    private String count;

    public ParentItem() {
    }

    public ParentItem(String month, String count) {
        this.month = month;
        this.count = count;
    }

    public String getMonth() {
        return month;
    }

    public String getCount() {
        return count;
    }
}
